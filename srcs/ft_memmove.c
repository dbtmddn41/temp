#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned int	i;

	if (!dst && !src)
		return (dst);
	i = 0;
	if (dst < src)
	{
		while (i < len)
		{
			*((char *)dst + i) = *((char *)src + i);
			i++;
		}
	}
	else
	{
		while (i < len)
		{
			*((char *)dst + len - 1 - i) = *((char *)src + len - 1 - i);
			i++;
		}
	}
	return (dst);
}
